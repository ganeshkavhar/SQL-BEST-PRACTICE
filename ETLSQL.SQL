--EMPLOYEE WHO GET HIGHEST SALARY
SELECT EMPLOYEE_ID,MAX(SALARY)
FROM EMPLOYEES
GROUP BY EMPLOYEE_ID;

SELECT * FROM
(SELECT DENSE_RANK() OVER(ORDER BY SALARY DESC) AS DRK,E.* FROM EMPLOYEES E)
WHERE DRK=1;

--DEPARTMENT WISE HIGHEST SALARY
SELECT DEPARTMENT_ID,MAX(SALARY)
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID;

--EMP JOIN IN MARCH
SELECT * FROM EMPLOYEES
WHERE TO_CHAR(HIRE_DATE,'MON')='MAR';

--ANNUAL SALARY OF EACH EMPLOYEES
SELECT FIRST_NAME,SALARY*12 AS ANNSAL
FROM EMPLOYEES;

--THIRD HIGHEST SALARY
SELECT * FROM
(SELECT DENSE_RANK() OVER (ORDER BY SALARY DESC) AS DRK,E.* FROM EMPLOYEES E)
WHERE DRK=3;

--DISPLAY 9 THRECORDS
SELECT * FROM 
(SELECT ROWNUM AS RN,E.* FROM EMPLOYEES E)
WHERE RN=9;

--DIPLICATES RECORDS FROM TABLES
SELECT ROWID FROM EMPLOYEES
WHERE ROWID NOT IN
(SELECT MAX(ROWID) FROM EMPLOYEES GROUP BY EMPLOYEE_ID);

--FOR SINGLE COLUMN CHECK
SELECT EMAIL,COUNT(*)
FROM EMPLOYEES
GROUP BY EMAIL
HAVING COUNT(*)>1;


--SELECT RECORDS FROM EMPLOYEES
SELECT DISTINCT * FROM EMPLOYEES;

--DISPLAY 50% RECORDS FROM EMPLOYEES
SELECT * FROM EMPLOYEES
WHERE ROWNUM<=(SELECT COUNT(*)/2 FROM EMPLOYEES);

--DISPLAY LAST 50% RECORDS FROM EMPLOYEES
SELECT * FROM EMPLOYEES
WHERE ROWNUM<=(SELECT COUNT(*)/2 FROM EMPLOYEES)
ORDER BY EMPLOYEE_ID;

--RECORD OF EMP CONTEIN 'A'
SELECT * FROM EMPLOYEES
WHERE FIRST_NAME LIKE '%A%';

--EMPLOYEES AND TOTAL SALARY
SELECT EMPLOYEE_ID,SUM(SALARY)
FROM EMPLOYEES
GROUP BY EMPLOYEE_ID;

--CREATE COPY OF EMP TABE
CREATE TABLE SV AS SELECT * FROM EMPLOYEES;



